package com.example.ailatrieuphu;

public class BangXepHang {
    private int id;
    private String name;
    private int soLevel;
    private int soTien;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getSoLevel() {
        return soLevel;
    }

    public void setSoLevel(int soLevel) {
        this.soLevel = soLevel;
    }

    public int getSoTien() {
        return soTien;
    }

    public void setSoTien(int soTien) {
        this.soTien = soTien;
    }
}
